let player;

function onYouTubeIframeAPIReady(event) {
  console.log("onYouTubeIframeAPIReady!!!");
  if (typeof event == "undefined") {
    console.log("why doe it load before click??");
    return;
  }
  console.log("onYouTubeIframeAPIRead");
  let dataId = event.currentTarget.getAttribute("data-src");
  let dataNumber = event.currentTarget.getAttribute("data-number");
  let ids = event.currentTarget.id;
  let playerToogle = document.getElementById("player" + dataNumber);
  playerToogle.style.display = "block";
  console.log(ids + ", " + dataNumber + ", " + dataId);

  player = new YT.Player("player" + dataNumber, {
    height: "360",
    width: "640",
    videoId: dataId,
    playerVars: {
      autoplay: 1,
      controls: 1,
      rel: 0,
      fs: 0,
      theme: "light",
      color: "white",
      mute: 0,
      allow: "autoplay",
    },
    events: {
      onReady: onPlayerReady,
      onStateChange: onPlayerStateChange,
    },
  });
}

// 4. The API will call this function when the video player is ready.
function onPlayerReady(event) {
  event.target.playVideo();
}

// 5. The API calls this function when the player's state changes.
//    The function indicates that when playing a video (state=1),
//    the player should play for six seconds and then stop.
var done = false;
function onPlayerStateChange(event) {
  if (event.data == YT.PlayerState.PLAYING && !done) {
    // setTimeout(stopVideo, 6000);
    done = true;
  }
}
function stopVideo() {
  player.stopVideo();
}
